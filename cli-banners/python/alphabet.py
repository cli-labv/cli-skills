ALPHABET = {
    'block': BLOCK,
    'slim': SLIM,
    'mini': MINI,
    'simple': SIMPLE,
}

# Style metadata
STYLE_INFO = {
    'block': {'height': 6, 'description': 'Bold block letters with box-drawing characters'},
    'slim': {'height': 5, 'description': 'Thinner block letters'},
    'mini': {'height': 3, 'description': 'Compact 3-line letters'},
    'simple': {'height': 5, 'description': 'Pure ASCII, no unicode'},
}


