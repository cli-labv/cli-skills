def get_letter(char: str, style: str = 'block') -> List[str]:
    """
    Get the ASCII art representation of a single character.
    
    Args:
        char: Single character to get
        style: Font style ('block', 'slim', 'mini', 'simple')
    
    Returns:
        List of strings representing the character
    
    Example:
        >>> lines = get_letter('A', 'block')
        >>> for line in lines:
        ...     print(line)
    """
    char = char.upper()
    font = ALPHABET.get(style, BLOCK)
    
    if char in font:
        return font[char]
    
    # Return space for unknown characters
    return font.get(' ', ['   '] * STYLE_INFO.get(style, {}).get('height', 6))


def get_style_height(style: str) -> int:
    """Get the line height for a style."""
    return STYLE_INFO.get(style, {}).get('height', 6)


def render_text(
    text: str,
    style: str = 'block',
    color: str = None,
    spacing: int = 1,
) -> str:
    """
    Render text as ASCII art banner.
    
    Args:
        text: Text to render
        style: Font style ('block', 'slim', 'mini', 'simple')
        color: ANSI color name (optional)
        spacing: Space between characters
    
    Returns:
        Multi-line string with ASCII art
    
    Example:
        >>> banner = render_text("HELLO", style='block', color='cyan')
        >>> print(banner)
    """
    text = text.upper()
    height = get_style_height(style)
    
    # Build lines
    lines = ['' for _ in range(height)]
    space = ' ' * spacing
    
    for char in text:
        char_lines = get_letter(char, style)
        
        for i in range(height):
            if i < len(char_lines):
                lines[i] += char_lines[i] + space
            else:
                lines[i] += ' ' * len(char_lines[0]) + space
    
    result = '\n'.join(lines)
    
    # Apply color if specified
    if color:
        result = colorize(result, color)
    
    return result


def colorize(text: str, color: str) -> str:
    """Apply ANSI color to text."""
    colors = {
        'red': '\033[31m',
        'green': '\033[32m',
        'yellow': '\033[33m',
        'blue': '\033[34m',
        'magenta': '\033[35m',
        'cyan': '\033[36m',
        'white': '\033[37m',
        'bright_red': '\033[91m',
        'bright_green': '\033[92m',
        'bright_yellow': '\033[93m',
        'bright_blue': '\033[94m',
        'bright_cyan': '\033[96m',
    }
    
    reset = '\033[0m'
    code = colors.get(color.lower(), '')
    
    if code:
        return f"{code}{text}{reset}"
    return text


def list_characters(style: str = 'block') -> List[str]:
    """List all available characters for a style."""
    font = ALPHABET.get(style, BLOCK)
    return sorted(font.keys())


def preview_alphabet(style: str = 'block') -> str:
    """
    Generate a preview of the complete alphabet.
    
    Args:
        style: Font style to preview
    
    Returns:
        Multi-line string showing A-Z
    """
    lines = []
    
    # Split into rows for readability
    rows = ['ABCDEFGHI', 'JKLMNOPQR', 'STUVWXYZ', '0123456789']
    
    for row in rows:
        lines.append(render_text(row, style))
        lines.append('')
    
    return '\n'.join(lines)


